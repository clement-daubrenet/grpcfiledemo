#include <algorithm>
#include <chrono>
#include <cmath>
#include <iostream>
#include <memory>
#include <string>

#include <grpc/grpc.h>
#include <grpcpp/server.h>
#include <grpcpp/server_builder.h>
#include <grpcpp/server_context.h>
#include <grpcpp/security/server_credentials.h>
#include "grpcfiledemo.grpc.pb.h"

using grpc::Server;
using grpc::ServerBuilder;
using grpc::ServerContext;
using grpc::ServerReader;
using grpc::ServerReaderWriter;
using grpc::ServerWriter;
using grpc::Status;
using grpcfiledemo::Feature;
using grpcfiledemo::Content;
using grpcfiledemo::RouteGuide;
using std::chrono::system_clock;



class RouteGuideImpl final : public RouteGuide::Service {
  Status ChunkChat(ServerContext* context,
                   ServerReaderWriter<Content, Content>* stream) override {
    std::vector<Content> received_contents;
    Content content;
    while (stream->Read(&content)) {
      for (const Content& c : received_contents) {
          std::cout << "Server received " << c.message() << std::endl;
          stream->Write(c);
      }
      received_contents.push_back(content);
    }

    return Status::OK;
  }

 private:
  std::vector<Feature> feature_list_;
};

void RunServer() {
  std::string server_address("0.0.0.0:50051");
  RouteGuideImpl service;

  ServerBuilder builder;
  // Listen on the given address without any authentication mechanism.
  builder.AddListeningPort(server_address, grpc::InsecureServerCredentials());
  // Register "service" as the instance through which we'll communicate with
  // clients. In this case it corresponds to an *synchronous* service.
  builder.RegisterService(&service);
  // Finally assemble the server.
  std::unique_ptr<Server> server(builder.BuildAndStart());
  std::cout << "Server listening on " << server_address << std::endl;

  // Wait for the server to shutdown. Note that some other thread must be
  // responsible for shutting down the server for this call to ever return.
  server->Wait();
}

int main(int argc, char** argv) {
  // Expect only arg: --db_path=path/to/route_guide_db.json.
  RunServer();

  return 0;
}
